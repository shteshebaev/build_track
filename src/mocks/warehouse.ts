export const getStockStatus = (quantity: number, minStock: number): 'in_stock' | 'low_stock' | 'out_of_stock' => {
  if (quantity === 0) return 'out_of_stock'
  if (quantity < minStock) return 'low_stock'
  return 'in_stock'
}
